function handoffNote({ role, task, provider, model, usage, recentDecisions }) {
  const ts = new Date().toISOString();
  const { total, limit, pct } = usage;
  const totalFmt = total.toLocaleString();
  const limitFmt = limit.toLocaleString();

  const decisionLines = (recentDecisions || [])
    .slice(-5)
    .map((d) => `- [${d.ts}] ${d.role}: ${d.task} (${d.output_lines} output lines)`)
    .join("\n") || "  (no prior decisions in this session)";

  return [
    `# Agent Handoff — ${ts}`,
    "",
    `> This handoff was generated automatically because the context window reached **${pct}% capacity**.`,
    `> Start a fresh session and include this note so the agent can pick up where you left off.`,
    "",
    "## Session context",
    "",
    `| Field | Value |`,
    `|---|---|`,
    `| Role | \`${role}\` |`,
    `| Task | ${task} |`,
    `| Provider | ${provider} |`,
    `| Model | ${model} |`,
    `| Tokens used | ${totalFmt} / ${limitFmt} (${pct}%) |`,
    "",
    "## What was accomplished (last 5 decisions)",
    "",
    decisionLines,
    "",
    "## Prompt for your new session",
    "",
    "Paste this at the start of your next `agent-contract run` or conversation:",
    "",
    "```",
    `I am continuing a previous agent-contract session. Context window was at ${pct}%.`,
    `Role: ${role}`,
    `Task: ${task}`,
    ``,
    `Previous decisions are logged in .agent/memory/decisions.jsonl.`,
    `Pick up from where we left off and do not repeat completed work.`,
    "```",
    "",
    `---`,
    `_Generated by agent-contract token manager at ${ts}_`,
    "",
  ].join("\n");
}

