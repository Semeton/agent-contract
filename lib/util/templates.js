function readmeForAgentDir() {
  return `# .agent/

Deterministic contract layer for coding agents. This directory is the **single source of truth** for how AI agents operate in this repository.

Any agent (Claude, Codex, Copilot, Antigravity, etc.) entering this repo MUST:

1. Read \`manifest.yaml\` first.
2. Load \`stack.yaml\` to understand the technology context.
3. Load \`conventions.yaml\` to understand engineering rules.
4. Load the relevant role from \`roles/\` for the current task.
5. Run the appropriate check from \`checks/\` before and after generating output.
6. Append non-trivial decisions to \`memory/decisions.jsonl\`.

Humans: see \`conventions.yaml\` to change policy. Re-run \`agent-contract init\` after dependency changes to refresh \`stack.yaml\`.
`;
}

