function stackYaml(detection) {
  const body = {
    schema: "agent-contract/v1",
    generated_by: "agent-contract detect",
    confidence: detection.confidence,
    language: detection.language,
    runtime: detection.runtime,
    framework: detection.framework,
    package_manager: detection.package_manager,
    test_runner: detection.test_runner,
    build: detection.build,
    lint: detection.lint,
    formatter: detection.formatter,
    db: detection.db,
    orm: detection.orm,
    entry_points: detection.entry_points || [],
    unresolved: detection.unresolved || [],
    evidence: detection.evidence || [],
  };
  const header =
    "# stack.yaml — generated by `agent-contract`.\n" +
    "# Edit the values; re-running detect will preserve manual overrides if you mark them.\n" +
    "# Fields under `unresolved` could not be auto-detected and SHOULD be filled in.\n\n";
  return header + stringify(body);
}

