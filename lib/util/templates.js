function preGenerateCheck() {
  return `#!/usr/bin/env bash
# pre-generate.sh — gate before the generator runs.
# Hard fails if required inputs are missing.
# Usage: pre-generate.sh <task-spec.json>
set -euo pipefail

SPEC="\${1:-}"
if [[ -z "$SPEC" || ! -f "$SPEC" ]]; then
  echo "pre-generate: missing task spec file" >&2
  exit 2
fi

required=(feature_name input_shape output_shape error_cases)
missing=()
for k in "\${required[@]}"; do
  if ! grep -q "\\"$k\\"" "$SPEC"; then
    missing+=("$k")
  fi
done

if (( \${#missing[@]} > 0 )); then
  echo "pre-generate: HARD FAIL — missing required fields: \${missing[*]}" >&2
  echo "agent must ask the human for these before proceeding." >&2
  exit 1
fi

echo "pre-generate: OK"
`;
}

function postGenerateCheck(detection) {
  const stackChecks = buildStackChecks(detection);
  return `#!/usr/bin/env bash
# post-generate.sh — runs after the generator emits files.
# Checks: scope, size${stackChecks ? ", lint, typecheck, tests" : ""}.
# Hard fails on any violation.
set -euo pipefail

ROOT="$(cd "$(dirname "\${BASH_SOURCE[0]}")/.." && pwd)"
PROJECT_ROOT="$(cd "$ROOT/.." && pwd)"

# Gather changed files (empty when not a git repo)
changed=""
if [[ -d "$PROJECT_ROOT/.git" ]]; then
  changed=$(git -C "$PROJECT_ROOT" diff --name-only --cached 2>/dev/null || git -C "$PROJECT_ROOT" diff --name-only || true)
fi

# Scope check: diff must be within generator's allowed paths.
if [[ -n "$changed" ]]; then
  while IFS= read -r f; do
    [[ -z "$f" ]] && continue
    case "$f" in
      src/*|tests/*) ;;
      *)
        echo "post-generate: HARD FAIL — file outside generator scope: $f" >&2
        exit 1
        ;;
    esac
  done <<< "$changed"
fi

# Size check (max 300 LOC per file under src/).
if [[ -d "$PROJECT_ROOT/src" ]]; then
  while IFS= read -r f; do
    lines=$(wc -l < "$f")
    if (( lines > 300 )); then
      echo "post-generate: HARD FAIL — $f exceeds 300 lines ($lines)" >&2
      exit 1
    fi
  done < <(find "$PROJECT_ROOT/src" -type f \\( -name "*.ts" -o -name "*.js" -o -name "*.php" -o -name "*.py" -o -name "*.go" \\))
fi
${stackChecks}
echo "post-generate: OK"
`;
}

function buildStackChecks(detection) {
  if (!detection || !detection.commands) return "";
  const { commands } = detection;
  const parts = [];

  parts.push('\ncd "$PROJECT_ROOT"');

  if (commands.lint && commands.lint_ext) {
    const pat = commands.lint_ext.map((e) => `*${e}`).join("|");
    parts.push(
      `\n# Lint (${detection.lint})`,
      "lint_files=\"\"",
      "while IFS= read -r f; do",
      `  case "$f" in ${pat}) lint_files="$lint_files $f" ;; esac`,
      `done <<< "\${changed:-}"`,
      "if [[ -n \"$lint_files\" ]]; then",
      `  ${commands.lint} $lint_files || { echo "post-generate: HARD FAIL — lint (${detection.lint})" >&2; exit 1; }`,
      "fi",
    );
  }

  if (commands.typecheck) {
    parts.push(
      `\n# Typecheck (${detection.language})`,
      `${commands.typecheck} || { echo "post-generate: HARD FAIL — typecheck" >&2; exit 1; }`,
    );
  }

  if (commands.test) {
    if (commands.test_files && commands.lint_ext) {
      const pat = commands.lint_ext.map((e) => `*${e}`).join("|");
      parts.push(
        `\n# Tests (${detection.test_runner} — changed files only)`,
        "test_files=\"\"",
        "while IFS= read -r f; do",
        `  case "$f" in ${pat}) test_files="$test_files $f" ;; esac`,
        `done <<< "\${changed:-}"`,
        "if [[ -n \"$test_files\" ]]; then",
        `  ${commands.test} $test_files || { echo "post-generate: HARD FAIL — tests (${detection.test_runner})" >&2; exit 1; }`,
        "fi",
      );
    } else {
      parts.push(
        `\n# Tests (${detection.test_runner})`,
        `${commands.test} || { echo "post-generate: HARD FAIL — tests (${detection.test_runner})" >&2; exit 1; }`,
      );
    }
  }

  return parts.join("\n");
}

function debugScopeCheck() {
  return `#!/usr/bin/env bash
# debug-scope.sh — enforces "fix, don't refactor" rule.
# Compares proposed changes against the file list in the debug report.
# Hard fails if changes touch files outside the diagnosed scope.
set -euo pipefail

REPORT="\${1:-}"
if [[ -z "$REPORT" || ! -f "$REPORT" ]]; then
  echo "debug-scope: missing debug report" >&2
  exit 2
fi

# Extract files listed under "## Affected Files" in the report.
declared=$(awk '
  /^## Affected Files/ { in_section=1; next }
  /^## / && in_section { in_section=0 }
  in_section && /^- / { print substr($0, 3) }
' "$REPORT" | sed 's/:.*//')

if [[ -z "$declared" ]]; then
  echo "debug-scope: HARD FAIL — no affected files declared in report" >&2
  exit 1
fi

PROJECT_ROOT="$(cd "$(dirname "\${BASH_SOURCE[0]}")/../.." && pwd)"
changed=$(git -C "$PROJECT_ROOT" diff --name-only 2>/dev/null || true)

violations=()
while IFS= read -r f; do
  [[ -z "$f" ]] && continue
  if ! grep -qxF "$f" <<< "$declared"; then
    violations+=("$f")
  fi
done <<< "$changed"

if (( \${#violations[@]} > 0 )); then
  echo "debug-scope: HARD FAIL — changes outside diagnosed scope:" >&2
  printf '  %s\\n' "\${violations[@]}" >&2
  echo "Either expand the diagnosis (and re-justify) or remove these changes." >&2
  exit 1
fi

echo "debug-scope: OK"
`;
}

