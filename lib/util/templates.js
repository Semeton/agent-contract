  lines.push("");

  // Paradigm
  if (style) {
    lines.push(`# paradigm — inferred (confidence: ${style.confidence})`);
    lines.push(`# evidence: ${style.evidence.class_files} class file(s), ${style.evidence.func_files} function-only file(s)`);
    lines.push(`paradigm: ${style.paradigm}`);
  } else {
    lines.push("# paradigm — could not infer; set manually: oop | functional | mixed");
    lines.push("paradigm: oop");
  }
  lines.push("");

  lines.push("module_size:");
  lines.push("  max_lines_per_file: 300    # default — not inferred");
  lines.push("  max_lines_per_function: 40");
  lines.push("  max_params: 4");
